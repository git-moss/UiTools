package de.mossgrabers.tools.ui.control.loggerbox;

/**
 * The logging levels.
 *
 * @author J&uuml;rgen Mo&szlig;graber
 */
public enum LoggerBoxLevel
{
    /** Level for fine grained debug output. */
    DEBUG,
    /** Level for normal informational output. */
    INFO,
    /** Level for a non critical warning. */
    WARN,
    /** Level for critical errors. */
    ERROR
}
